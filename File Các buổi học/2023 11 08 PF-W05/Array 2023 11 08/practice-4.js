// Bài 4: Cho 2 array có cùng số phần tử với nhau. 
let array1 = [10, 20, 30, 40];
let array2 = [100, 200, 300, 400];

let reverseArray = array2.reverse();
document.write(`${array1[0]} ${reverseArray[0]} <br> ${array1[1]} ${reverseArray[1]} <br> ${array1[2]} ${reverseArray[2]} <br> ${array1[3]} ${reverseArray[3]}`);
// Hãy in các thành phần trong 2 array theo format dưới đây (sử dụng document.write)
/**
 * 10 400
 * 20 300
 * 30 200
 * 40 100
 */